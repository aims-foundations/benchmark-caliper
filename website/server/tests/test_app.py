def _start_run_to_awaiting(
    client: TestClient, fake_pdf: bytes, opted_in_full: bool = True
) -> tuple[str, str, str]:
    """Drive /api/runs to the awaiting_answers state and return
    (run_id, metadata_text, slug)."""
    with patch.object(
        app_module, "call_text_async", side_effect=make_call_results()
    ):
        r = post_run(client, fake_pdf, opted_in_full=opted_in_full)
    assert r.status_code == 200
    events = parse_sse(r.text)
    qcomplete = next(
        e for n, e in events if n == "step-completed" and e["step"] == "2-questions"
    )
    out = qcomplete["output"]
    return out["run_id"], out["metadata"], out["slug"]


def _post_answers(
    client: TestClient,
    run_id: str,
    *,
    api_key: str | None = FAKE_KEY,
    metadata: str = "- name: sea-helm\n",
    questions: list[dict] | None = None,
    answers: list[dict] | None = None,
):
    headers = {}
    if api_key is not None:
        headers["X-Anthropic-Key"] = api_key
    body = {
        "deployment_description": "Indonesian government chatbot",
        "metadata": metadata,
        "questions": questions if questions is not None else SAMPLE_QUESTIONS,
        "answers": (
            answers
            if answers is not None
            else [{"id": q["id"], "answer": f"answer for {q['id']}"} for q in SAMPLE_QUESTIONS]
        ),
        "opted_in_full": True,
    }
    return client.post(
        f"/api/runs/{run_id}/answers",
        headers=headers,
        json=body,
    )


