def test_run_emits_expected_sse_events(
    client: TestClient, fake_pdf: bytes
) -> None:
    results = make_call_results()
    with patch.object(
        app_module, "call_text_async", side_effect=results
    ):
        r = post_run(client, fake_pdf)
    assert r.status_code == 200
    assert r.headers["content-type"].startswith("text/event-stream")

    events = parse_sse(r.text)
    names = [name for name, _ in events]
    assert names == [
        "step-started",
        "step-completed",
        "step-started",
        "step-completed",
        "step-started",
        "step-completed",
        "awaiting-answers",
    ]

    # Step 0 output
    assert events[1] == (
        "step-completed",
        {"step": "0-slug", "output": {"slug": "indonesia_bahasa_administrative"}},
    )
    # Step 1 output (metadata text passed through)
    assert events[3][0] == "step-completed"
    assert events[3][1]["step"] == "1-metadata"
    assert "sea-helm" in events[3][1]["output"]["metadata"]
    # Step 2 output: parsed questions, plus echoed metadata + run_id + slug
    assert events[5][0] == "step-completed"
    assert events[5][1]["step"] == "2-questions"
    out = events[5][1]["output"]
    assert out["questions"] == SAMPLE_QUESTIONS
    assert "sea-helm" in out["metadata"]
    assert out["slug"] == "indonesia_bahasa_administrative"
    assert isinstance(out["run_id"], str) and len(out["run_id"]) > 0
    # Awaiting-answers terminator
    assert events[6][0] == "awaiting-answers"
    assert events[6][1]["slug"] == "indonesia_bahasa_administrative"


